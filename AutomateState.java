package com.company;

public class AutomateState
{
    protected int id1, id2, id3;

    public AutomateState(int id1, int id2, int id3) {
        this.id1 = id1;
        this.id2 = id2;
        this.id3 = id3;
    }

    public int getId1() {
        return id1;
    }

    public int getId2() {
        return id2;
    }

    public int getId3() {
        return id3;
    }
}
